import { useState } from "react";
import { Form, Button, Alert } from "react-bootstrap";

export default function AboutContentEditor() {
  const defaultText = `Открытый кампус РГПУ им. А. И. Герцена — это новое пространство в исторических зданиях Герценовского университета, где проходят образовательные и научные мероприятия не только для студентов, но и для жителей и гостей города.

Уже сегодня свои двери распахнула Открытая гостиная на Казанской, 1, где проводятся лекции, выставки, мастер-классы, круглые столы и открытые семинары. Также в состав кампусного комплекса входят: 6-й корпус Герценовского университета, сквер, старый сад, книжный магазин. 
Совсем скоро они откроют для всех желающих участвовать в жизни кампуса. Здесь будут располагаться большие лекционные залы, зоны отдыха, открытые библиотеки, галереи, коворкинги.

В Открытом кампусе Герценовского университета вы найдете лекции от ведущих специалистов высшего и дополнительного образования, научные интерактивы, мастер-классы, круглые столы на разнообразные темы широкого спектра профессиональных сфер. Сегодня открытые кампусы при университетах становятся важной частью городской среды. Это места общения, образования, совместной работы специалистов разных областей науки и образования.

Кампусы помогают сформировать новую инфраструктуру в высшем и дополнительном образовании, реализовать передовые образовательные и научные проекты, создают условия для совместной работы российских и иностранных студентов и преподавателей.

Важнейшей особенностью кампусов является создание единой среды студенческого, научного и бизнес-сообщества, интегрированного в городскую среду и востребованную жителями города.`;

  const [text, setText] = useState(defaultText);
  const [saved, setSaved] = useState(false);

  const handleSave = () => {
    setSaved(true);
    setTimeout(() => setSaved(false), 3000); // Сбрасываем уведомление через 3 секунды
  };

  return (
    <div className="mb-5">
      <h3>Редактор текста "О пространстве"</h3>
      {saved && <Alert variant="success">Текст успешно сохранён!</Alert>}
      <Form.Group className="mb-3" controlId="aboutContent">
        <Form.Control
          as="textarea"
          rows={15}
          style={{ minHeight: "400px" }}
          value={text}
          onChange={(e) => setText(e.target.value)}
        />
      </Form.Group>
      <Button variant="primary" onClick={handleSave}>
        Сохранить
      </Button>
    </div>
  );
}
